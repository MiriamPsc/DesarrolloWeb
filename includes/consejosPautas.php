                        <h2>Condicta Prohibida</h2>                        
                        <p class="text-muted">Las políticas de visitas en la Institución Jeffersonian están diseñadas para proteger a los visitantes y al personal, así como para asegurar y preservar las colecciones, las instalaciones y los edificios históricos confiados al cuidado de la Jeffersonian.</p>
                        
                        <h6 class="text-muted">Mientras se encuentre en las instalaciones del Smithsonian, NINGÚN VISITANTE DEBE:</h6>
                        <ul class="text-muted">
                            <li>Portar armas de fuego, otras armas peligrosas o mortales, o explosivos, ya sea abiertamente u ocultos en las instalaciones. La prohibición de armas de fuego se aplica a todos los visitantes, incluidos aquellos con permisos de armas de fuego, pero no al personal policial activo autorizado a portar armas de fuego.</li>
                            <li>Exhibir o llevar pancartas, letreros o pancartas.</li>
                            <li>Destruir, dañar o quitar propiedad.</li>
                            <li>Escalar sobre cualquier parte de un edificio.</li>
                            <li>Usar un lenguaje fuerte, abusivo o inapropiado.</li>
                            <li>Holgazanear, dormir o participar en asambleas injustificadas.</li>
                            <li>Crear cualquier peligro para personas o cosas.</li>
                            <li>Realizar actos obscenos o indecentes.</li>
                            <li>Participar en conducta desordenada.</li>
                            <li>Usar, poseer o vender drogas ilegales.</li>
                            <li>Traer perros u otros animales, que no sean animales de servicio.</li>
                            <li>Tomar fotografías con fines publicitarios o cualquier otro fin comercial.</li>
                            <li>Solicitar con fines comerciales o benéficos o distribuir anuncios, panfletos, prospectos y volantes.</li>
                        </ul>

                        <h6 class="text-muted">Además de lo anterior, mientras se encuentre en las instalaciones del zoológico, NINGÚN VISITANTE DEBERÁ:</h6>
                        <ul class="text-muted">
                            <li>Matar, herir o molestar a cualquier animal de exhibición o de investigación por cualquier medio, excepto para garantizar la seguridad personal.</li>
                            <li>Acariciar, intentar acariciar, manipular, mover o retirar animales de exhibición o de investigación.</li>
                            <li>Alimentar a los animales de exhibición o de investigación, excepto en estricta conformidad con las señales autorizadas.</li>
                            <li>Atrapar, intentar atrapar, atrapar, eliminar o matar a cualquier animal que deambule libremente y que habite en las instalaciones.</li>
                            <li>Pasar por encima, por debajo, entre o cruzar cualquier barandilla, cerca, foso, pared o cualquier otra barrera de seguridad.</li>
                            <li>Sentar, parar o sostener a los niños sobre cualquier barrera de seguridad.</li>
                            <li>Lanzar o arrojar piedras, o cualquier otro proyectil hacia, desde o mientras se encuentre en las instalaciones.</li>
                            <li>Llevar carriolas, cochecitos de bebé u otros medios de transporte, excepto sillas de ruedas, a los edificios de exhibición y baños públicos.</li>
                            <li>Participar en juegos de pelota o cualquier actividad atlética, excepto en lugares designados oficialmente para tales fines.</li>
                            <li>Fumar o llevar cigarrillos, cigarros o pipas encendidos a los edificios de exhibición, o hacer fuego de cualquier tipo en las instalaciones.</li>
                            <li>Dañar, desfigurar, recoger o quitar cualquier hierba, arbusto, arbusto, árbol o césped, o parte del mismo.</li>
                        </ul>
                        <p class="text-muted">Los visitantes que violen estas reglas pueden estar sujetos a multas o prisión. Las regulaciones completas de edificios y terrenos del Smithsonian se pueden encontrar en el Título 36 del Código de Regulaciones Federales, partes 504 y 520 (Zoológico Nacional).</p>

